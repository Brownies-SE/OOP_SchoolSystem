public class Teachers {

    private int id;
    private String name;
    private int salary;

    public Teachers( int id, String name, int salary) {
        this.id = id;
        this.name = name;
        this.salary = salary;
    }

    public getId() {
        return id;
    }

    public getName() {
        return name;
    }

    public getSalary() {
        return salary;
    }
}